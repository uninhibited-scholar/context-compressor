"""Reproducible benchmark: token reduction across representative inputs.

Runs the default compressor over several synthetic-but-realistic datasets and
emits ``BENCHMARKS.md`` (a table) and ``benchmark_chart.svg`` (a bar chart).
Zero external dependencies — the SVG is hand-rendered.

    python benchmarks/benchmark.py
"""

from __future__ import annotations

import os
import sys
import time

sys.path.insert(0, os.path.join(os.path.dirname(__file__), "..", "src"))

from context_compressor import ContextCompressor  # noqa: E402

HERE = os.path.dirname(__file__)


# -- datasets -------------------------------------------------------------
_PATHS = ["/api/users", "/api/orders", "/health", "/login", "/api/cart",
          "/static/app.js", "/api/search", "/metrics", "/api/payments"]


def app_log(n: int = 400) -> str:
    # Realistic: mostly-unique request lines (varying path/status/latency/bytes)
    # interleaved with genuine noise (timestamps, progress, status chatter).
    rows = []
    for i in range(n):
        path = _PATHS[i % len(_PATHS)]
        status = (200, 200, 200, 404, 500)[i % 5]
        rows.append(f"2024-01-15 10:{i % 60:02d}:{(i * 7) % 60:02d} "
                    f"[INFO] {('GET', 'POST')[i % 2]} {path} {status} "
                    f"{(i * 13) % 900 + 5}ms {(i * 31) % 4096}b uid={i % 97}")
        if i % 4 == 0:
            rows.append("Connecting to upstream service...")
        if i % 9 == 0:
            rows.append(f"[==========> ] {(i % 10) * 10}%")
    rows.append("ERROR: disk full on /var, write failed")
    return "\n".join(rows)


def scan_output(n: int = 300) -> str:
    # Realistic: many distinct hosts/ports with periodic exact-duplicate
    # re-reports (scanners re-emit findings across passes).
    rows = ["Starting scan 10.0.0.0/24",
            "2024-01-15 09:00:00 nmap scan initiated"]
    ports = (22, 80, 443, 8080, 3306, 5432)
    for i in range(n):
        host = f"10.0.{i // 255}.{i % 255}"
        rows.append(f"{host} port {ports[i % len(ports)]} open")
        if i % 5 == 0:  # re-report (duplicate-able)
            rows.append(f"{host} port {ports[i % len(ports)]} open")
        if i % 40 == 0:
            rows.append(f"SQL Injection at https://x.local/item?id={i}")
    return "\n".join(rows)


def agent_transcript(n: int = 120) -> str:
    # Realistic: mostly-unique reasoning, plus recurring boilerplate phrasing.
    topics = ["orders", "payments", "inventory", "auth", "search", "billing"]
    rows = []
    for i in range(n):
        t = topics[i % len(topics)]
        rows.append(f"Assistant: Step {i}: inspecting the {t} service latency "
                    f"and comparing it against the {(i * 3) % 500}ms baseline.")
        if i % 3 == 0:  # recurring boilerplate the PatternRemover catches
            rows.append("Assistant: I have already queried the database for "
                        "the relevant metrics.")
    rows.append("Assistant: Final answer: the bottleneck is a missing index.")
    return "\n".join(rows)


def json_dump(n: int = 60) -> str:
    items = ",".join(
        f'{{"id": {i}, "ua": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) '
        f'AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120 Safari/537.36"}}'
        for i in range(n)
    )
    return f'{{"rows": [{items}], "count": {n}}}'


DATASETS = {
    "Application log": app_log,
    "Security scan": scan_output,
    "Agent transcript": agent_transcript,
    "JSON result dump": json_dump,
}


def run():
    compressor = ContextCompressor()
    results = []
    for name, factory in DATASETS.items():
        text = factory()
        t0 = time.perf_counter()
        if name == "JSON result dump":
            res = compressor.compress_json(text)
        else:
            res = compressor.compress(text)
        elapsed_ms = (time.perf_counter() - t0) * 1000
        results.append({
            "name": name,
            "before": res.stats.original_tokens,
            "after": res.stats.compressed_tokens,
            "reduction": res.stats.reduction_pct,
            "ms": elapsed_ms,
            "backend": res.stats.token_backend,
        })
    return results


def write_markdown(results):
    lines = [
        "# Benchmarks",
        "",
        "Generated by `python benchmarks/benchmark.py`. Token counts use the "
        f"`{results[0]['backend']}` backend.",
        "",
        "| Dataset | Tokens before | Tokens after | Reduction | Time |",
        "|---|--:|--:|--:|--:|",
    ]
    for r in results:
        lines.append(
            f"| {r['name']} | {r['before']:,} | {r['after']:,} | "
            f"**{r['reduction']:.1f}%** | {r['ms']:.1f} ms |"
        )
    avg = sum(r["reduction"] for r in results) / len(results)
    lines += ["", f"**Average token reduction: {avg:.1f}%**", "",
              "![Token reduction chart](benchmark_chart.svg)", ""]
    path = os.path.join(HERE, "BENCHMARKS.md")
    with open(path, "w", encoding="utf-8") as fh:
        fh.write("\n".join(lines))
    return path


def write_svg(results):
    width, bar_h, gap, left, top = 760, 34, 18, 190, 50
    height = top + len(results) * (bar_h + gap) + 40
    max_w = width - left - 90
    parts = [
        f'<svg xmlns="http://www.w3.org/2000/svg" width="{width}" '
        f'height="{height}" font-family="system-ui, sans-serif">',
        f'<rect width="{width}" height="{height}" fill="#0d1117"/>',
        f'<text x="{left}" y="30" fill="#e6edf3" font-size="18" '
        f'font-weight="700">Token reduction by dataset</text>',
    ]
    y = top
    for r in results:
        bw = max(2, int(max_w * r["reduction"] / 100))
        parts.append(
            f'<text x="{left - 12}" y="{y + bar_h * 0.66}" fill="#9da7b3" '
            f'font-size="13" text-anchor="end">{r["name"]}</text>')
        parts.append(
            f'<rect x="{left}" y="{y}" width="{max_w}" height="{bar_h}" '
            f'rx="5" fill="#21262d"/>')
        parts.append(
            f'<rect x="{left}" y="{y}" width="{bw}" height="{bar_h}" rx="5" '
            f'fill="#2ea043"/>')
        parts.append(
            f'<text x="{left + bw + 10}" y="{y + bar_h * 0.66}" fill="#e6edf3" '
            f'font-size="13" font-weight="600">{r["reduction"]:.1f}%</text>')
        y += bar_h + gap
    parts.append("</svg>")
    path = os.path.join(HERE, "benchmark_chart.svg")
    with open(path, "w", encoding="utf-8") as fh:
        fh.write("\n".join(parts))
    return path


def main():
    results = run()
    for r in results:
        print(f"{r['name']:<20} {r['before']:>6} -> {r['after']:>5} tokens "
              f"({r['reduction']:5.1f}%)  {r['ms']:.1f} ms")
    md = write_markdown(results)
    svg = write_svg(results)
    print(f"\nWrote {os.path.relpath(md)} and {os.path.relpath(svg)}")


if __name__ == "__main__":
    main()
